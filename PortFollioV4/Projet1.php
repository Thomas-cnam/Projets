    <div class="PresentationProjet1">
      <h1>Présentation</h1>
      <p>Un projet réalisé en JAVAFX en groupe de deux : le but est d'avoir une interface qui permet de piloter quatres
        caméras différentes
        et de pouvoir effectuer différentes actions </p>
    </div>

    <div class="ContexteProjet1">
      <h1>Contexte</h1>
      <p>Le contexte est : M. LEDUC a constaté depuis quelque temps la disparition de marchandises dans le
        magasin. </p>
        <p> Il a donc décidé de mettre son magasin sous vidéosurveillance. Bien sûr, il en a
        averti ses employés et il a placé des panneaux d’information à destination du public dans
        le magasin. </p>
        <p> Des caméras D-Link DCS-5222L ont été installées dans les locaux et elles sont visibles
        sur le plan du magasin </p>
    </div>

    <div class="ConditionProjet1">
      <h1>Conditions</h1>
      <p> Le logiciel doit permettre : </p> 
      <p> - De piloter jusqu’à quatre caméras dans une même fenêtre </p>
      <p> - de modifier la position d’une caméra (droite, gauche, haut, bas) </p>
      <p> - de faire faire un balayage automatique à une caméra </p>
      <p> - d’enregistrer sur le disque les images provenant d’une caméra </p>
      <p> - de consulter les images enregistrées sur le disque. </p>

    </div>

    <div class="UtilisationProjet1">
      <h1>Utilisation</h1>

